<div class="label label-primary">Pembaruan Peta NKRI terkait ZEE</div><br/><br/>
<p>Kementerian Koordinator Bidang Kemaritiman melakukan pembaruan peta Negara Kesatuan Republik Indonesia (NKRI). Poin pembaruan di antaranya menyangkut batas zona ekonomi eksklusif (ZEE). Sebelum peta baru tersebut diluncurkan, sebelumnya terdapat perjanjian perbatasan terlebih dahulu yang ditandatangani oleh kedua belah negara, yaitu Indonesia dan Singapura. Selain itu, ada juga perjanjian batas maritim antara Indonesia dan Filipina. </p></div>


